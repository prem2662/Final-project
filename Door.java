public class Door
{
    int doorNo;
    String doorName;
    public Door(int dno, String dname)
    {
        // initialise instance variables
        this.doorNo = dno;
        this.doorName = dname;
    }

    public void setdoorNo(int dno)
    {
        this.doorNo = dno;
        
    }
    public void setdoorName(String dname)
    {
        this.doorName = dname;
        
    }
}
